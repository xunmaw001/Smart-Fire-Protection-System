			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																
																																				// 下2
			res = await this.$api.option(`yonghu`,`yonghuming`,{});
			this.yonghumingOptions = res.data;
																					
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fangwuxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='louhao'){
					this.ruleForm.louhao = obj[o];
					this.ro.louhao = true;
					continue;
					}
					if(o=='menpaihao'){
					this.ruleForm.menpaihao = obj[o];
					this.ro.menpaihao = true;
					continue;
					}
